public class User_Bean {
    private String user_id;
    private String user_name;
    private String password;
    private String name_in_game;
    private String phone;
    private String star_amount;

    /**
     * @return the user_name
     */
    public String getUser_name() {
        return user_name;
    }

    /**
     * @param user_name the user_name to set
     */
    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the name_in_game
     */
    public String getName_in_game() {
        return name_in_game;
    }

    /**
     * @param name_in_game the name_in_game to set
     */
    public void setName_in_game(String name_in_game) {
        this.name_in_game = name_in_game;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the star_amount
     */
    public String getStar_amount() {
        return star_amount;
    }

    /**
     * @param star_amount the star_amount to set
     */
    public void setStar_amount(String star_amount) {
        this.star_amount = star_amount;
    }

    /**
     * @return the user_id
     */
    public String getUser_id() {
        return user_id;
    }

    /**
     * @param user_id the user_id to set
     */
    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    
}
